// Function to search cities
export const searchCities = (query) => {
    if (!query || query.length < 2) return [];

    const searchTerm = query.toLowerCase().trim();

    return indianCities
        .filter(location =>
            location.city.toLowerCase().includes(searchTerm) ||
            location.state.toLowerCase().includes(searchTerm)
        )
        .slice(0, 10); // Limit to 10 results
};

// Function to search cities including custom cities from Firestore
export const searchCitiesWithCustom = async (query, customCities = []) => {
    if (!query || query.length < 2) return [];

    const searchTerm = query.toLowerCase().trim();

    // Search in static database
    const staticResults = indianCities
        .filter(location =>
            location.city.toLowerCase().includes(searchTerm) ||
            location.state.toLowerCase().includes(searchTerm)
        );

    // Search in custom cities
    const customResults = customCities
        .filter(location =>
            location.city.toLowerCase().includes(searchTerm) ||
            location.state.toLowerCase().includes(searchTerm)
        )
        .map(loc => ({ ...loc, isCustom: true })); // Mark as custom

    // Combine and limit results
    return [...staticResults, ...customResults].slice(0, 10);
};

// Function to get unique states
export const getStates = () => {
    const uniqueStates = [...new Set(indianCities.map(loc => loc.state))];
    return uniqueStates.sort();
};

// Function to get cities by state
export const getCitiesByState = (stateName) => {
    return indianCities
        .filter(loc => loc.state === stateName)
        .map(loc => loc.city)
        .sort();
};
